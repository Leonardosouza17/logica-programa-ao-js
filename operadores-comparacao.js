const a = 'a', b = 'A';
const condicao = a == b;

// true -> palavra reservada para o valor "verdadeiro"
// false -> palavra reservada para o valor "falso"
// == -> é igual
// === -> é estritamente igual (valor e tipo)
// != -> não é igual
// !== não é estritamente igual (valor ou tipo)
// > maior que
// < menor que
// >= maior OU igual
// <= menor OU igual


//Estrutura IF-ELSE

if (condicao) { 
    console.log('VERDADEIRO');
} else {
    console.log('FALSO');
}